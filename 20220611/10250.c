#include <stdio.h>

int main()
{
    int T;
    int H;
    int W;
    int N;
    int i;

    i = 0;
    scanf("%d", &T);
    while (i < T)
    {
        scanf("%d %d %d", &H, &W, &N);
        if (N % H == 0)
            printf("%d%02d\n", H, N / H);
        else
            printf("%d%02d\n", N % H, N / H + 1);
        i++;
    }
    return (0);
}
