#include <stdio.h>

int main()
{
    int     M;
    int     N;
    int     i;
    int     j;
    int    arr[1000001] = { 0 };
    
    scanf("%d %d", &M, &N);
    arr[0] = 1;
    arr[1] = 1;

    i = 2;
    while (i <= N)
    {
        j = 2 * i;
        while (j <= N)
        {
            if (arr[j] == 0)
                arr[j] = 1;
            j = j + i;
        }
        i++;
    }
    while (M <= N)
    {
        if (arr[M] == 0)
            printf("%d\n", M);
        M++;
    }
}
