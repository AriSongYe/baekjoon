#include <stdio.h>

int main()
{
    int A;
    int B;
    int C;

    scanf("%d %d %d", &A, &B, &C);
    if (B >= C)
        printf("%d", -1);
    else
        printf("%d", A/(C-B) + 1);
    return (0);
}
